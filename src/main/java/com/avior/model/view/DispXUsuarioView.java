package com.avior.model.view;

import java.io.Serializable;
import java.sql.Timestamp;

public class DispXUsuarioView implements Serializable{

	private static final long serialVersionUID = 1L;
	
	private String alias;
	
	private Long nroSerie;
	
	private String contrato;
	
	private Timestamp fechaVenta;
	
	private String tipoDispositivo;
	
	private Integer fiIdTipo;

	private Boolean status;
	
	private Boolean estacionamiento;
	
	
	
	public Boolean getStatus() {
		return status;
	}

	public void setStatus(Boolean status) {
		this.status = status;
	}

	public Integer getFiIdTipo() {
		return fiIdTipo;
	}

	public void setFiIdTipo(Integer fiIdTipo) {
		this.fiIdTipo = fiIdTipo;
	}

	public String getTipoDispositivo() {
		return tipoDispositivo;
	}

	public void setTipoDispositivo(String tipoDispositivo) {
		this.tipoDispositivo = tipoDispositivo;
	}

	public String getAlias() {
		return alias;
	}

	public void setAlias(String alias) {
		this.alias = alias;
	}

	public Long getNroSerie() {
		return nroSerie;
	}

	public void setNroSerie(Long nroSerie) {
		this.nroSerie = nroSerie;
	}

	public String getContrato() {
		return contrato;
	}

	public void setContrato(String contrato) {
		this.contrato = contrato;
	}

	public Timestamp getFechaVenta() {
		return fechaVenta;
	}

	public void setFechaVenta(Timestamp fechaVenta) {
		this.fechaVenta = fechaVenta;
	}

	public Boolean getEstacionamiento() {
		return estacionamiento;
	}

	public void setEstacionamiento(Boolean estacionamiento) {
		this.estacionamiento = estacionamiento;
	}
	
	
	

}

package com.avior.model.notifications;

public class AjaxNotification {
	private String userMessage;
	private Integer status=0;
	private String otherInfo;
	private long regAfectados;
	
	public String getUserMessage() {
		return userMessage;
	}
	public void setUserMessage(String userMessage) {
		this.userMessage = userMessage;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getOtherInfo() {
		return otherInfo;
	}
	public void setOtherInfo(String otherInfo) {
		this.otherInfo = otherInfo;
	}
	
	
	
	public long getRegAfectados() {
		return regAfectados;
	}
	public void setRegAfectados(long regAfectados) {
		this.regAfectados = regAfectados;
	}
	public AjaxNotification(String userMessage, Integer status, String otherInfo) {
		super();
		this.userMessage = userMessage;
		this.status = status;
		this.otherInfo = otherInfo;
	}
	public AjaxNotification() {
		super();
	}
	
	
}
